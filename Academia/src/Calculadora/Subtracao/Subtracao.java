package Calculadora.Subtracao;

public class Subtracao {

    double entrada1;
    double entrada2;
    double resultado;

    public Subtracao(double entrada1, double entrada2) {
        this.entrada1 = entrada1;
        this.entrada2 = entrada2;
    }

    public void Resultrado(double entrada1, double entrada2){
        resultado = entrada1 + entrada2;
    }
}
