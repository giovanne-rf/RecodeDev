package Calculadora.Divisao;

public class Divisao {


    double entrada1;
    double entrada2;
    double resultado;

    public Divisao(double entrada1, double entrada2) {
        this.entrada1 = entrada1;
        this.entrada2 = entrada2;
    }

    public void Resultado(double entrada1, double entrada2){
        resultado = entrada1*entrada2;
    }
}
